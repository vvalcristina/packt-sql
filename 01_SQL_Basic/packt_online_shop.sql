create database PACKT_ONLINE_SHOP;

use PACKT_ONLINE_SHOP;

create table Customers
(
    FirstName varchar(50) ,
    MiddleName varchar(50) ,
    LastName varchar(50) ,
    HomeAddress varchar(250) ,
    Email varchar(200) ,
    Phone varchar(50) ,
    Notes varchar(250)
);

select * from Customers;